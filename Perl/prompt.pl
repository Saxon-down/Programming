###############################################################################
#                                                                             #
# USED Libraries                                                              #
#                                                                             #
###############################################################################

use Term::ANSIColor qw(:constants);


###############################################################################
#                                                                             #
# Constants                                                                   #
#                                                                             #
###############################################################################


###############################################################################
#                                                                             #
# Variables                                                                   #
#                                                                             #
###############################################################################

my ($user, $path, $host, $hist);


###############################################################################
#                                                                             #
# Subroutines                                                                 #
#                                                                             #
###############################################################################


###############################################################################
#                                                                             #
# MAIN                                                                        #
#                                                                             #
###############################################################################

{
  my ($user, $path, $host, $hist) = split /##/, $ARGV[0];
  print BOLD, WHITE, "(";
  print BOLD, RED, $hist;
  print BOLD, WHITE, ")[";
  print BOLD, YELLOW, $user;
  print BOLD, WHITE, "@";
  print BOLD, GREEN, $host;
  print BOLD, WHITE, ":";
  print BOLD, BLUE, $path;
  print BOLD, WHITE, "]\n> ";
  print RESET, "";
}
