sub Extract_Baselines {			# $project
# Takes a Project and extracts a list of baselines that it's made of
  my $project = $_[0];
  my @details = qx/cleartool lsproject -l $project/;
  my $found_baselines = 0;
  foreach my $line (@details) {
    if ($line =~ /^  recommended baselines:/) {
	  $found_baselines = 1;
    } elsif ($line =~ /^  \w/) {
	  $found_baselines = 0;
    } elsif $found_baselines {
      push (@baselines, $line);	  
    }	
  }
}

